    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        
        fileChooser.setCurrentDirectory(new File(".")); //chọn file hiện tại mà mình đang mở

        int response = fileChooser.showSaveDialog(null); //chọn file để save
        //Vận dụng vào select File nha
        if (response == JFileChooser.APPROVE_OPTION) {
            File file = new File(fileChooser.getSelectedFile().getAbsolutePath());
            System.out.println(file);
            
            String noiDung = txtNoiDung.getText();
            try {
                BufferedWriter bw = new BufferedWriter(new FileWriter(file));
                bw.write(noiDung);
                bw.newLine();
                bw.close();
                JOptionPane.showMessageDialog(this, "Luu thanh cong");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Loi!");
            }
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

