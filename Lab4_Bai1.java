    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        JFileChooser fileChooser1 = new JFileChooser();
        
        fileChooser1.setCurrentDirectory(new File(".")); //chọn file hiện tại mà mình đang mở

        int response = fileChooser1.showOpenDialog(null); //chọn tệp để mở

        //Vận dụng vào select File nha
        if (response == JFileChooser.APPROVE_OPTION) {
            File file = new File(fileChooser1.getSelectedFile().getAbsolutePath());
            System.out.println(file);
            txtNoiDung.setText("");
            try {
                BufferedReader br = new BufferedReader(new FileReader(file));
                while (true) {
                    String line = br.readLine();
                    if (line == null) {
                        break;
                    } else {
                        txtNoiDung.append(line);
                        txtNoiDung.append("\r\n");
                    }
                }
                br.close();
//                JOptionPane.showMessageDialog(this, " Doc File thanh cong");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Loi doc File");
            }
            // dùng biến file này để làm công việc khác nhé
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

